package packageSourceCode;

public class ArithmaticClass {
	
	public int methodAdd(int a, int b)
	{
		return a + b ;
	}
	
	public int methodSub(int a, int b)
	{
		return a - b ;
	}
	
	public int methodMul(int a, int b)
	{
		return a * b ;
	}
	
	public int methodDiv(int a, int b)
	{
		return a / b ;
	}

}
